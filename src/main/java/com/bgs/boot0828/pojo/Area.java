package com.bgs.boot0828.pojo;


public class Area {

  private long id;
  private String name;
  private long pId;
  private String level;

  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public long getpId() {
    return pId;
  }

  public void setpId(long pId) {
    this.pId = pId;
  }

  public long getPId() {
    return pId;
  }

  public void setPId(long pId) {
    this.pId = pId;
  }

  public String getLevel() {
    return level;
  }

  public void setLevel(String level) {
    this.level = level;
  }
}
